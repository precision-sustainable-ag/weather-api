CREATE OR REPLACE FUNCTION nldas(
  p_lat   double precision,
  p_lon   double precision,
  p_start timestamptz,
  p_end   timestamptz
)
RETURNS TABLE (
  lat double precision,
  lon double precision,
  date timestamptz,
  air_temperature real,
  humidity real,
  pressure real,
  zonal_wind_speed real,
  meridional_wind_speed real,
  longwave_radiation real,
  convective_precipitation real,
  potential_energy real,
  potential_evaporation real,
  precipitation real,
  shortwave_radiation real,
  relative_humidity real,
  wind_speed real,
  nswrs real,
  nlwrs real,
  dswrf real,
  dlwrf real,
  lhtfl real,
  shtfl real,
  gflux real,
  snohf real,
  asnow real,
  arain real,
  evp real,
  ssrun real,
  bgrun real,
  snom real,
  avsft real,
  albdo real,
  weasd real,
  snowc real,
  snod real,
  tsoil real,
  soilm1 real,
  soilm2 real,
  soilm3 real,
  soilm4 real,
  soilm5 real,
  mstav1 real,
  mstav2 real,
  soilm6 real,
  evcw real,
  trans real,
  evbs real,
  sbsno real,
  cnwat real,
  acond real,
  ccond real,
  lai real,
  veg real
)
LANGUAGE sql
STABLE
AS $$
  SELECT
    lat,
    lon,
    date,
    air_temperature,
    humidity,
    pressure,
    zonal_wind_speed,
    meridional_wind_speed,
    longwave_radiation,
    convective_precipitation,
    potential_energy,
    potential_evaporation,
    precipitation,
    shortwave_radiation,
    relative_humidity,
    wind_speed,
    nswrs,
    nlwrs,
    dswrf,
    dlwrf,
    lhtfl,
    shtfl,
    gflux,
    snohf,
    asnow,
    arain,
    evp,
    ssrun,
    bgrun,
    snom,
    avsft,
    albdo,
    weasd,
    snowc,
    snod,
    tsoil,
    soilm1,
    soilm2,
    soilm3,
    soilm4,
    soilm5,
    mstav1,
    mstav2,
    soilm6,
    evcw,
    trans,
    evbs,
    sbsno,
    cnwat,
    acond,
    ccond,
    lai,
    veg
  FROM weather
  WHERE
    lat = p_lat AND lon = p_lon
    AND date BETWEEN p_start AND p_end
$$;

